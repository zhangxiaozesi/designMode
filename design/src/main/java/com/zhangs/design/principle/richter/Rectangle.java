package com.zhangs.design.principle.richter;

public class Rectangle {
    //宽
    private int wide;
    //长
    private int length;


    public int getWide() {
        return wide;
    }

    public void setWide(int wide) {
        this.wide = wide;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }


}
